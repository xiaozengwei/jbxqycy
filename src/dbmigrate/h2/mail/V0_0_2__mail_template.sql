

-------------------------------------------------------------------------------
--  mail template
-------------------------------------------------------------------------------
CREATE TABLE MAIL_TEMPLATE(
        ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL,
        NAME VARCHAR(50),
	RECEIVER VARCHAR(200),
	SENDER VARCHAR(200),
	CC VARCHAR(200),
	BCC VARCHAR(200),
	SUBJECT VARCHAR(200),
	CONTENT VARCHAR(65535),
	MANUAL INT,
	SCOPE_ID VARCHAR(50),
        CONSTRAINT PK_MAIL_TEMPLATE PRIMARY KEY(ID)
);

